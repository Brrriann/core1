def cmd_preview():
    """변경 사항 미리보기 — 파일/git 변경 없음"""
    target = Path(TARGET_FILE)
    if not target.exists():
        log(f"파일을 찾을 수 없습니다: {TARGET_FILE}", "❌")
        sys.exit(1)

    content = target.read_text(encoding="utf-8")
    _, changes = apply_replacements(content)

    print("\n" + "═"*60)
    print(" 📋  DRY-RUN 미리보기  (실제 변경 없음)")
    print("═"*60 + "\n")
    print_diff(changes)
    print("═"*60)
    log("실제 적용하려면:  python deploy_copy.py --apply\n")


def cmd_apply():
    """파일 수정 → 백업 → git add/commit/push"""
    target = Path(TARGET_FILE)
    if not target.exists():
        log(f"파일을 찾을 수 없습니다: {TARGET_FILE}", "❌")
        sys.exit(1)

    print("\n" + "═"*60)
    print(" 🚀  카피 수정 + 배포 시작")
    print("═"*60 + "\n")

    # 1. 백업
    backup = make_backup(target)
    log(f"백업 완료: {backup}", "💾")

    # 2. 치환
    content = target.read_text(encoding="utf-8")
    modified, changes = apply_replacements(content)

    if not changes:
        log("변경할 항목이 없습니다. FIND 텍스트를 확인해 주세요.", "⚠️")
        sys.exit(0)

    target.write_text(modified, encoding="utf-8")
    log(f"HTML 수정 완료: {len(changes)}개 항목 적용\n", "✅")
    print_diff(changes)

    # 3. git add
    code, _, err = run_git(["add", str(target)])
    if code != 0:
        log(f"git add 실패: {err}", "❌"); sys.exit(1)
    log("git add 완료", "📁")

    # 4. git commit
    code, out, err = run_git(["commit", "-m", COMMIT_MSG])
    if code != 0:
        log(f"git commit 실패: {err}", "❌"); sys.exit(1)
    log(f"git commit 완료: {COMMIT_MSG}", "📝")

    # 5. git push
    code, out, err = run_git(["push", "origin", GIT_BRANCH])
    if code != 0:
        log(f"git push 실패: {err}", "❌")
        log("push는 수동으로 실행해 주세요: git push origin main")
        sys.exit(1)
    log(f"git push 완료 → origin/{GIT_BRANCH}", "☁️")

    print("\n" + "═"*60)
    log("Cloudflare Pages 빌드가 자동으로 시작됩니다.", "🌐")
    log(f"롤백이 필요하면: python deploy_copy.py --rollback", "↩️")
    print("═"*60 + "\n")


def cmd_rollback():
    """최신 백업으로 복원 → git add/commit/push"""
    target = Path(TARGET_FILE)
    backup = get_latest_backup()

    if not backup:
        log(f"백업 파일이 없습니다. ({BACKUP_DIR} 폴더 확인)", "❌")
        sys.exit(1)

    print("\n" + "═"*60)
    print(" ↩️   롤백 시작")
    print("═"*60 + "\n")
    log(f"복원 대상 백업: {backup}", "💾")

    shutil.copy2(backup, target)
    log(f"파일 복원 완료: {TARGET_FILE}", "✅")

    code, _, err = run_git(["add", str(target)])
    if code != 0:
        log(f"git add 실패: {err}", "❌"); sys.exit(1)

    msg = f"revert: 카피 수정 롤백 ({backup.name})"
    code, _, err = run_git(["commit", "-m", msg])
    if code != 0:
        log(f"git commit 실패: {err}", "❌"); sys.exit(1)
    log(f"git commit: {msg}", "📝")

    code, _, err = run_git(["push", "origin", GIT_BRANCH])
    if code != 0:
        log(f"git push 실패: {err}", "❌"); sys.exit(1)
    log(f"git push 완료 → origin/{GIT_BRANCH}", "☁️")

    print("\n" + "═"*60)
    log("롤백 배포 완료. Cloudflare Pages가 자동으로 복원됩니다.", "🌐")
    print("═"*60 + "\n")


