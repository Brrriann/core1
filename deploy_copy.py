def log(msg: str, emoji: str = ""):
    prefix = f"{emoji} " if emoji else "   "
    print(f"{prefix}{msg}")


def run_git(args: list) -> tuple[int, str, str]:
    result = subprocess.run(
        ["git"] + args,
        capture_output=True, text=True, encoding="utf-8"
    )
    return result.returncode, result.stdout.strip(), result.stderr.strip()


def check_git_status() -> bool:
    code, out, _ = run_git(["status", "--porcelain"])
    return code == 0


def make_backup(src: Path) -> Path:
    os.makedirs(BACKUP_DIR, exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    dest = Path(BACKUP_DIR) / f"index.{ts}.html"
    shutil.copy2(src, dest)
    return dest


def get_latest_backup() -> Path | None:
    d = Path(BACKUP_DIR)
    if not d.exists():
        return None
    backups = sorted(d.glob("index.*.html"), reverse=True)
    return backups[0] if backups else None


def apply_replacements(content: str) -> tuple[str, list]:
    changes = []
    for find, replace in REPLACEMENTS:
        if find in content:
            count = content.count(find)
            content = content.replace(find, replace)
            changes.append((find, replace, count))
    return content, changes


def print_diff(changes: list):
    if not changes:
        log("변경 항목 없음 — FIND 텍스트가 파일에서 발견되지 않았습니다.", "⚠️")
        log("HTML 소스의 실제 텍스트와 REPLACEMENTS의 FIND 값을 비교해 보세요.")
        return
    log(f"총 {len(changes)}개 항목 변경 예정:\n")
    for i, (find, replace, count) in enumerate(changes, 1):
        f_short = find[:55]   + ("…" if len(find)    > 55 else "")
        r_short = replace[:55]+ ("…" if len(replace) > 55 else "")
        print(f"  [{i:02d}] ({count}곳)")
        print(f"        🔴 {f_short!r}")
        print(f"        🟢 {r_short!r}")
        print()


